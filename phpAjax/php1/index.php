            <h3>Ugo Fantozzi</h3>
            <p>
              Il personaggio esordisce nel 1968 nella trasmissione che segna
              l'esordio televisivo di Paolo Villaggio, Quelli della domenica,
              anche se veniva citato in terza persona nei monologhi dell'artista
              genovese. Negli sketch di Quelli della domenica, Villaggio era
              solito raccontare storie comiche e catastrofiche, col suo lessico
              particolarissimo, fondato sull'iperbole di cui il protagonista era
              spesso lo sventurato Fantozzi. Fantozzi era il cognome di un
              impiegato nella grande azienda (la Italsider) dove aveva lavorato
              Villaggio nonché il suo vicino di scrivania. Si faceva trascinare
              nelle iniziative dello stesso Villaggio, dal Fantozzi chiamato
              erroneamente "Selvaggio", situazione che anticipa il ruolo di
              organizzatore di viaggi che sarà di Fracchia e del Ragionier Filini
              nei racconti e nei film del personaggio.
              Tra i colleghi di Fantozzi, infatti, nei racconti sono presenti
              anche queste due figure, due personaggi che convivono nei primi due
              libri di Villaggio, per poi fondersi, grazie a un'intuizione del
              regista Luciano Salce, nell'unico personaggio di Filini, che avrà il
              ruolo dell'organizzatore, del massacratore emotivo di ciascun
              impiegato e di maniaco che crede di intendersi di tutto, nei film e
              nei successivi libri di Villaggio.
              Da queste storie Villaggio scriverà dei racconti per l'Europeo, che
              verranno raccolti nel libro Fantozzi (1971) che diventa ben presto
              un bestseller, (più di un milione di copie vendute)[1], venendo
              anche tradotta in molte lingue. Infatti l'attore genovese vince, in
              Unione Sovietica, il premio Gogol come "miglior scrittore in
              cirillico", nella sezione "migliore opera umoristica"[2]. Nel 2011,
              per i 150 anni dell'Unità d'Italia, il libro è stato scelto dal
              comitato scientifico del Centro per il libro e la lettura tra i 150
              libri che hanno segnato la storia del nostro paese.[3] Visto il
              grande successo, si pensa ad una trasposizione cinematografica, e
              alla fine il ruolo del protagonista sarà rivestito da Paolo
              Villaggio stesso, che presterà il suo volto a quella che è la sua
              maschera più famosa e apprezzata.
              Nel 1975 esce il primo film, dal semplice titolo Fantozzi, tratto
              dai primi due libri del personaggio e diretto da Luciano Salce.
